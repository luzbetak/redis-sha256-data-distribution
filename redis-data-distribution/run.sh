#!/bin/bash
#--------------------------------------------------------------------------------------#
if [ -z "$1" ]; then
    echo "+----------------------------------------+"
    echo "|  ./run.sh   -- Help                    |"
    echo "|  ./run.sh 0 -- build project           |"
    echo "|  ./run.sh 1 -- run                     |"
    echo "+----------------------------------------+"
#--------------------------------------------------------------------------------------#
elif [ "$1" == "0" ] 2>/dev/null; then
    mvn clean compile assembly:single 

#--------------------------------------------------------------------------------------#
elif [ "$1" == "1" ] 2>/dev/null; then
    mvn exec:java -Dexec.mainClass=com.kduraj.distribution.app.App

fi
#--------------------------------------------------------------------------------------#

